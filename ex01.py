# 1.	Faça um programa que leia um número inteiro e verifique se ele é par ou ímpar.

# Entrada de dados
num = int(input("Digite um número inteiro: "))
# Processamento
if num % 2 == 0:
    print("O número é par")
else:
    print("O número é ímpar")
    
# Saída de dados
print("Fim do programa")
